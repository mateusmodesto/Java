    private void btnigualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnigualActionPerformed
       //Botão do Igual
        n2 = Double.parseDouble(visor.getText());  // ao apertar o botão de igual, o nuúmero digitado no visor será adicionado a variável n2
       switch (modo){
               case 1 -> visor.setText(String.valueOf(n1+n2));
               case 2 -> visor.setText(String.valueOf(n1-n2));
               case 3 -> visor.setText(String.valueOf(n1*n2));
               case 4 -> visor.setText(String.valueOf(n1/n2));
       }     
       n1 = n2 = numeros = 0; //após tudo, reseta todas as variáveis
       digito = "";
    }//GEN-LAST:event_btnigualActionPerformed

