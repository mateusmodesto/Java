    private void btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn1ActionPerformed
        //botão número 1
        if (numeros == 0){ // a variavel numeros é usada para verificar se nenhum número foi apertado, deste modo:
            visor.setText("1");  //se nenhum foi, ele substituirá o 0 pelo 1
            numeros++; // e somará a variável numeros mais 1, deste modo, na próxima verificação o sistema entederá que já há e não precisará substituir  
        }
        else{
            digito = visor.getText();  //se ja foi adicionado, o sistema pega o valor digitado
            visor.setText(digito + "1"); //e adiciona o valor pressionado (1) 
        }
    }//GEN-LAST:event_btn1ActionPerformed
    
